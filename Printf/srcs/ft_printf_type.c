#include "libftprintf.h"

int		ft_isspace(int c)
{
	return ((c == ' ') | (c == '\n') | (c == '\t') |
			(c == '\v') | (c == '\f') | (c == '\r'));
}

int		ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') | (c >= 'A' && c <= 'Z'));
}

int		ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}
